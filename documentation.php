SMS Gateway:
	
    GSM module.

Vendo UI:

    Main interface for the client. Dynamically updates.

butterfly:

    Terminal for the raspberry pi. Use default credentials for login.    

vendoadmin:

    Dashboard		Dynamic Panels
    Products 		Add, edit and delete products. Set price.
    Transaction  	View all transactions.
    Income		View all incomes.
    Notifications	View all notifications.
    Settings		Set settings.  

elFinder:

    root 		View root directory for raspbian os.
    framework		View all source code for this framework.
    files		Upload all images, videos and info for items.
			note: use the brand_name.extension for the file name
		       	      	images - .jpg (required)
			      	videos - .mp4 (required)
			      	info   - .txt (required)

