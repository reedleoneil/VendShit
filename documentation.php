Arduino Commands:

    q - Returns the credit.
    w - Increment the credit.
    e - Decrement the credit.
    r - Reset the credit.
    0 - None. Disable slot for item.
    1 - Vend item in slot 1.
    2 - Vend item in slot 2.
    3 - Vend item in slot 3.
    4 - Vend item in slot 4. 
    5 - Vend item in slot 5.	//extra lang to kung sakali

